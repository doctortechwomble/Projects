#Concatenate stings and numbers
print("20 days are " + str(50) + " minutes")
# f is used to mix stings and numbers (f = format)
print(f"20 days are {50} minutes")
# Calculation with f
print(f"20 days are {20 * 24 * 60} minutes")
#Same calculation for 35 days
print(f"50 days are {50 * 24 * 60} minutes")
#To calculation seconds
print(f"20 days are {20 * 24 * 60 * 60} seconds")
